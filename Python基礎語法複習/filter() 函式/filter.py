# filter() 函式 用於 過濾 元素
#
# 語法：filter( function , 可迭代物件 )，function為過濾條件

# *************************************************************************************************

# 過濾出”偶數”的物件
def isEven(x):
    return x % 2 == 0

even_num = list(filter(isEven, [1, 2, 3, 4, 5, 6]))
print(even_num)

